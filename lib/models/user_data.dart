class UserData {
  String uid;
  String? imagePath;
  String name;
  String email;
  String pronouns;
  String description;
  String frequentedLocations;

  UserData({
    required this.uid,
    required this.imagePath,
    required this.name,
    required this.email,
    required this.pronouns,
    required this.description,
    required this.frequentedLocations,
  });
}
